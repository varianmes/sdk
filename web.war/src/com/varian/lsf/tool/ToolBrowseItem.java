package com.varian.lsf.tool;

public class ToolBrowseItem {

	private String toolgroup;
	private String requiredQty;
	private String loggedQty;

	
	public String getToolgroup() {
		return toolgroup;
	}
	public void setToolgroup(String toolgroup) {
		this.toolgroup = toolgroup;
	}
	public String getRequiredQty() {
		return requiredQty;
	}
	public void setRequiredQty(String requiredQty) {
		this.requiredQty = requiredQty;
	}
	public String getLoggedQty() {
		return loggedQty;
	}
	public void setLoggedQty(String loggedQty) {
		this.loggedQty = loggedQty;
	}


}
